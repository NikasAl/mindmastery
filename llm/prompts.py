# Специальные промпты для разных типов задач
MATH_EXPRESSION_PROMPT = """Это математическое выражение для упрощения/вычисления.

Дополнительные требования:
1. Выдели все промежуточные вычисления
2. Отметь паттерны (например, a²-b², (a+b)²)
3. Включи навыки оценки порядка результата
4. Добавь навыки самопроверки (обратная операция)
"""

WORD_PROBLEM_PROMPT = """Это текстовая задача.

Дополнительные требования:
1. Включи навык "перевод текста → уравнение"
2. Добавь навык визуализации условия
3. Включи навык проверки размерности
4. Добавь навык переформулировки для проверки
"""

PHYSICS_PROBLEM_PROMPT = """Это физическая задача.

Дополнительные требования:
1. Включи навык анализа размерностей
2. Добавь навык оценки порядка величины
3. Включи навык визуализации физической ситуации
4. Добавь навык работы с формулами в уме
"""


def get_decomposition_prompt(task: str, task_type: str = "math") -> str:
    """Get appropriate decomposition prompt for task type."""
    base_prompt = DECOMPOSITION_PROMPT.format(task=task)

    if task_type == "math":
        return base_prompt + "\n\n" + MATH_EXPRESSION_PROMPT
    elif task_type == "word":
        return base_prompt + "\n\n" + WORD_PROBLEM_PROMPT
    elif task_type == "physics":
        return base_prompt + "\n\n" + PHYSICS_PROBLEM_PROMPT
    return base_prompt


def get_exercise_prompt(skill: dict, context: str, skill_id: str) -> str:
    """Get exercise generation prompt."""
    return EXERCISE_GENERATION_PROMPT.format(
        skill=skill,
        context=context,
        skill_id=skill_id
    )
